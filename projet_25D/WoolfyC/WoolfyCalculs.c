// only work if -2 * M_PI < add < 2 * M_PI
float angle_op(float angle, float add) {
    angle += add;
    if (angle > 2 * M_PI) {
        angle -= 2 * M_PI;
    } else if (angle < 0) {
        angle += 2 * M_PI;
    }
    return angle;
}

